#' Suma dos numeros
#'
#' Esta funcion `suma()` toma dos numeros  y los suma.
#'
#' Estos son detalles extras de la funcion.
#'
#' @param x un vector numérico
#' @param y un vector numérico
#'
#' @return
#' Un vector numerico con la suma de `x` e `y`.
#'
#' @examples
#' suma(2,2)
#' suma(1,10)
#'
#'
#'
#' @export
suma <- function(x, y) {
  return(x + y)
}
